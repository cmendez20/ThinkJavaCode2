public class Date {
    public static void main(String[] args) {
        String day = "Monday";
        int date = 25;
        String month = "August";
        int year = 2025;

        System.out.println(day);
        System.out.println(date);
        System.out.println(month);
        System.out.println(year);

        System.out.println("---------------------");

        System.out.println(day + ", " + month + " " + date + ", " + year);
    }
}
